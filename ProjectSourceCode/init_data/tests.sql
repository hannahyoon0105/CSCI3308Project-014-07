Pour the batter evenly into the prepared cake pans.

Bake in the preheated oven for 30-35 minutes, or until a toothpick inserted into the center of the cakes comes out clean.

Remove the cakes from the oven and allow them to cool in the pans for 10 minutes. Then, transfer the cakes to a wire rack to cool completely.

For the frosting:

In a large mixing bowl, beat the softened butter on medium speed until creamy and smooth.

Add the cocoa powder and powdered sugar gradually, alternating with the milk and vanilla extract. Beat on low speed until everything is well combined and the frosting is smooth and creamy.

If the frosting is too thick, add a little more milk. If its too thin, add more powdered sugar until you reach the desired consistency.

Assembly:

Once the cakes have cooled completely, place one layer on a serving plate or cake stand. Spread a layer of frosting evenly over the top.

Place the second layer on top of the frosting and spread the remaining frosting over the top and sides of the cake.

Decorate the cake with chocolate shavings, sprinkles, or any other decorations of your choice.

